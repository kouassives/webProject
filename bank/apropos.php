				<h2>HISTOIRE</h2>
				<p>Micro Finance Ivoire Transnational Incorporated (MFITI), société anonyme, est créée en 1985 comme holding bancaire à l’initiative de la Fédération des chambres de commerce d’Afrique de l’Ouest avec le soutien de la Communauté économique des États de l’Afrique de l’Ouest (CEDEAO). Au début des années 1980, l’industrie bancaire de l’Afrique de l’Ouest est dominée par les banques d’État et les banques étrangères. Il n’existe pratiquement aucune banque du secteur privé africain dans la région.

La Fédération ouest-africaine des chambres de commerce a favorisé et accompagné le lancement d’un projet pour la création d’un établissement bancaire régional privé en Afrique de l’Ouest. Ecopromotions S.A. est créée en 1984. Ses actionnaires fondateurs ont réuni le capital de départ destiné aux études de faisabilité et aux activités promotionnelles ayant conduit à la création d’ETI.

En octobre 1985, ETI est créée avec un capital autorisé de 100 millions $ US. Le capital initial versé de 32 millions $ EU est mobilisé auprès de plus de 1500 personnes et institutions des pays de l’Afrique de l’Ouest. Le principal actionnaire est le Fonds de coopération, de compensation et de développement (Fonds de la CEDEAO), bras financier de la CEDEAO.

ETI signe un accord de siège avec le gouvernement du Togo en1985, qui lui confère le statut d’organisation internationale, jouissant des droits et privilèges nécessaires lui permettant d’exercer en tant qu’institution régionale, avec un statut d’établissement financier non-résident. ETI débute ses activités avec sa première filiale au Togo en mars 1988.</p>
